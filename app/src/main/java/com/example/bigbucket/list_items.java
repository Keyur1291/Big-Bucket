package com.example.bigbucket;

public class list_items {

    String name, price;

    public list_items() {
    }

    public list_items(String name, String price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String enrollment) {
        this.price = price;
    }

}
